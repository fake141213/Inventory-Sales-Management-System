<?php
// ========================================
// FILE: logout.php - ออกจากระบบ
// ========================================
// ไฟล์นี้ใช้เพื่อเทิร์น Session ของผู้ใช้และเปลี่ยนไปหน้า Login
// เมื่อผู้ใช้คลิกปุ่ม "ออกจากระบบ" จะมาที่ไฟล์นี้

// ⚙️ เริ่ม Session
// - อ่านข้อมูล Session ที่บันทึกไว้
session_start();

// ✅ เชื่อมต่อฐานข้อมูล
include 'db.php';

// 🗑️ ลบ Session ทั้งหมด (ตัดการเชื่อมต่อผู้ใช้)
// - session_destroy() = เทิร์น SESSION ให้หายไป
// - ผู้ใช้จะต้อง login ใหม่เพื่อเข้าใช้ระบบ
session_destroy();

// ↩️ เปลี่ยนกลับไปหน้า Login
// - เปลี่ยนไปที่ Login.php เพื่อให้ผู้ใช้สามารถ login ได้อีกครั้ง
header("Location: Login.php");
exit();